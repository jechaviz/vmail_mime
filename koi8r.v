fn decode_koi8_r_bytes(bytes []u8) string {
	mut out := []u8{}
	for b in bytes {
		append_utf8_codepoint(mut out, koi8_r_codepoint(b))
	}
	return out.bytestr()
}

fn koi8_r_codepoint(value u8) int {
	if value >= 0xc0 && value <= 0xdf {
		return koi8_r_letter_codepoints[int(value - 0xc0)]
	}
	if value >= 0xe0 {
		return koi8_r_capital_codepoints[int(value - 0xe0)]
	}
	return match value {
		0xa3 { 0x0451 }
		0xb3 { 0x0401 }
		else { int(value) }
	}
}
