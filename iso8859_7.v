module vmail_mime

fn decode_iso_8859_7_bytes(bytes []u8) string {
	mut out := []u8{}
	for b in bytes {
		append_utf8_codepoint(mut out, iso_8859_7_codepoint(b))
	}
	return out.bytestr()
}

fn iso_8859_7_codepoint(value u8) int {
	match value {
		0xa1 { return 0x02bd }
		0xa2 { return 0x02bc }
		0xa4 { return 0x20ac }
		0xa5 { return 0x20af }
		0xaa { return 0x037a }
		0xaf { return 0x2015 }
		0xb4 { return 0x0384 }
		0xb5 { return 0x0385 }
		0xb6 { return 0x0386 }
		0xb8 { return 0x0388 }
		0xb9 { return 0x0389 }
		0xba { return 0x038a }
		0xbc { return 0x038c }
		0xbe { return 0x038e }
		0xbf { return 0x038f }
		0xc0 { return 0x0390 }
		0xc1 { return 0x0391 }
		0xc2 { return 0x0392 }
		0xc3 { return 0x0393 }
		0xc4 { return 0x0394 }
		0xc5 { return 0x0395 }
		0xc6 { return 0x0396 }
		0xc7 { return 0x0397 }
		0xc8 { return 0x0398 }
		0xc9 { return 0x0399 }
		0xca { return 0x039a }
		0xcb { return 0x039b }
		0xcc { return 0x039c }
		0xcd { return 0x039d }
		0xce { return 0x039e }
		0xcf { return 0x039f }
		0xd0 { return 0x03a0 }
		0xd1 { return 0x03a1 }
		0xd3 { return 0x03a3 }
		0xd4 { return 0x03a4 }
		0xd5 { return 0x03a5 }
		0xd6 { return 0x03a6 }
		0xd7 { return 0x03a7 }
		0xd8 { return 0x03a8 }
		0xd9 { return 0x03a9 }
		0xda { return 0x03aa }
		0xdb { return 0x03ab }
		0xdc { return 0x03ac }
		0xdd { return 0x03ad }
		0xde { return 0x03ae }
		0xdf { return 0x03af }
		0xe0 { return 0x03b0 }
		0xe1 { return 0x03b1 }
		0xe2 { return 0x03b2 }
		0xe3 { return 0x03b3 }
		0xe4 { return 0x03b4 }
		0xe5 { return 0x03b5 }
		0xe6 { return 0x03b6 }
		0xe7 { return 0x03b7 }
		0xe8 { return 0x03b8 }
		0xe9 { return 0x03b9 }
		0xea { return 0x03ba }
		0xeb { return 0x03bb }
		0xec { return 0x03bc }
		0xed { return 0x03bd }
		0xee { return 0x03be }
		0xef { return 0x03bf }
		0xf0 { return 0x03c0 }
		0xf1 { return 0x03c1 }
		0xf2 { return 0x03c2 }
		0xf3 { return 0x03c3 }
		0xf4 { return 0x03c4 }
		0xf5 { return 0x03c5 }
		0xf6 { return 0x03c6 }
		0xf7 { return 0x03c7 }
		0xf8 { return 0x03c8 }
		0xf9 { return 0x03c9 }
		0xfa { return 0x03ca }
		0xfb { return 0x03cb }
		0xfc { return 0x03cc }
		0xfd { return 0x03cd }
		0xfe { return 0x03ce }
		else { return int(value) }
	}
}
